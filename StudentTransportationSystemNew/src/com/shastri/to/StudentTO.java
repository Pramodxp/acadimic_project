package com.shastri.to;

public class StudentTO {

	private long slNo;
	private String stuname;
	private String stuusn;
	private String stuemail;
	private String stupass;

	private String lat;
	private String lon;
	private String address;
	private String stdPermission;
	private String driver;
	private String date;

	public long getSlNo() {
		return slNo;
	}

	public void setSlNo(long slNo) {
		this.slNo = slNo;
	}

	public String getStuname() {
		return stuname;
	}

	public void setStuname(String stuname) {
		this.stuname = stuname;
	}

	public String getStuusn() {
		return stuusn;
	}

	public void setStuusn(String stuusn) {
		this.stuusn = stuusn;
	}

	public String getStuemail() {
		return stuemail;
	}

	public void setStuemail(String stuemail) {
		this.stuemail = stuemail;
	}

	public String getStupass() {
		return stupass;
	}

	public void setStupass(String stupass) {
		this.stupass = stupass;
	}

	public String getLat() {
		return lat;
	}

	public void setLat(String lat) {
		this.lat = lat;
	}

	public String getLon() {
		return lon;
	}

	public void setLon(String lon) {
		this.lon = lon;
	}

	public String getAddress() {
		return address;
	}

	public void setAddress(String address) {
		this.address = address;
	}

	public String getStdPermission() {
		return stdPermission;
	}

	public void setStdPermission(String stdPermission) {
		this.stdPermission = stdPermission;
	}

	public String getDriver() {
		return driver;
	}

	public void setDriver(String driver) {
		this.driver = driver;
	}

	public String getDate() {
		return date;
	}

	public void setDate(String date) {
		this.date = date;
	}

}
